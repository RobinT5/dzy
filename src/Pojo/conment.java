package Pojo;

public class conment {
		int aid;
		int cid;
		String content;
		int type;
		public int getAid() {
			return aid;
		}
		public void setAid(int aid) {
			this.aid = aid;
		}
		public int getCid() {
			return cid;
		}
		public void setCid(int cid) {
			this.cid = cid;
		}
		public String getContent() {
			return content;
		}
		public void setContent(String content) {
			this.content = content;
		}
		public int getType() {
			return type;
		}
		public void setType(int type) {
			this.type = type;
		}
}
